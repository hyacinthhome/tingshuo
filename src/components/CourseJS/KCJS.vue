                <p align="left"><strong>B. </strong><strong>口语教学资源库 </strong>（ 04 年 6 月在教务处立项）： </p>
                <p align="left">口语教学 资源库 ( 或 语料库 ) 建设分四大模块 --- </p>
                <p align="left"><strong>第一库 </strong>（主库）由六部分组成： </p>
                <p align="left">·&nbsp; <strong>Phonetics clinic (</strong><strong>语音诊所 ) ： </strong>编集可供学生发现语病，改进语音语调及各种语音的素材。 </p>
                <p align="left">·&nbsp; <strong>Picture talking </strong><strong>（看图说话）： </strong>编集可供课堂使用，课后练习的各类图片，教学短片，培养学生就某一日常话题作描述和评论的能力。 </p>
                <p align="left">·&nbsp; <strong>Topic discussion </strong><strong>（主题评论）： </strong>编集可供课堂使用，课后练习及各种讨论（或辩论）的题目，帮助学生掌握会话策略，较好地表达个人意见，情感，观点，较清楚地陈述事实，事件，理由等。 </p>
                <p align="left">·&nbsp; <strong>Debate </strong><strong>（英语辩论）： </strong>编集可供课堂使用，课外参用的辩题和范例，帮助学生练习和掌握用英语辩论的基本语言技巧和思维方法。 </p>
                <p align="left">·&nbsp; <strong>English Presentation( </strong><strong>英语演讲 ) </strong>：编集可供课堂使用，课后模仿的英语演讲范例，帮助学生学会就一般或专业性话题作略长的发言，或用较简练的语言概括内容较长，语言稍难的文本和讲话。 </p>
                <p align="left">·&nbsp; <strong>Oral testing( </strong><strong>口语测试 ) </strong>：编集可供口语测试反映学生“一般水平”，“较好水平”和“更高水平”的三种级别的考卷和参考答案，提供学生自评 / 互评及教师评核的参照。现口语测试题库规模已达到八大口试类型 320 道题并配有参考答案。 </p>
                <p align="left"><strong>第二库 </strong>以口语课外活动为主题，编集可供课外模仿 、 操练的内容形式多种多样的声像资料（如英语歌曲 、 影视欣赏 、 小剧院 、 博物馆等 , 以满足各级层次学生对语言学习实践活动的需求。我们成功地研制开发了 英语口语 CAI 和教学软件积件库。 </p>
                <p align="left"><strong>1 </strong><strong>．英语口语 CAI </strong>基于单机并可适用局域网的大学英语口语 CAI 具有模块化 、 个性化和现代化技术的特色，向学生提供文 、 图 、 声 、 像等复合信息资源，让学生在模拟真实情境中，通过人机交互作用或同学协作学习等手段，进行有意义的口语建构实践。口语 CAI 由教师 、研究 生和语音实验室工作人员协力开发，软件边研制边在教学中试用并加以改进，使用效果反响良好。大学英语口语 CAI 较好地解决语言基础与口头综合运用能力的问题，个性化自主学习与课堂教学的问题，外语文化素质与英语考试的问题，以及分类指导、分级教学与一般要求的问题。 </p>
