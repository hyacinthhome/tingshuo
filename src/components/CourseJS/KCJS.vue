                <p align="left"><strong>( </strong><strong>三 ) 研究性课程的探索 </strong></p>
                <p align="left"><strong>2002/3 </strong><strong>— 2004/6 年 </strong>属我校大学英语精品课程的建设的第一阶段 , 我们完成了中级英语口语教材的编写和试用，初步探索和研究了口语课程大纲 、 教学大纲 、 教学内容 、 教学方法 和 测试评估的方式。 <strong>2004/6 — 2006/6 </strong>属我校大学英语精品课程建设的第二阶段，我们以口语教学 资料 库的建设为主线，研制开发大学英语口语 CAI 并投入使用，带动教学内容 、 方法，测试和教育手段的改革和优化，营造有益学生个性化 、 自主型学习的教学条件和环境。 <strong>2006/6-2008/6 </strong>属我校大学英语精品课程建设的第三阶段，我们将更好地组织力量进行口语教学和测试的实验，加强理论研究，努力探索将大学英语口语精品课程建设成 <strong>研究性课程 </strong>的成功经验，并以此为突破点带动大学英语教学模式的整体改革，进而在全国大学英语教学改革中起示范和推广作用。 </p>
                <p align="left">大学英语口语精品课程的建立有一定的先进性 、 前瞻性，在 2003 年 1 月高教部关于大学英语教学改革文件提出之前就已成立；课程顺应了教改对提高学生听说能力的要求。大学英语口语精品课程经过五年的建设，获得了可贵的提高中国大学生口语的教学和测试评估的经验。然我们尚须进一步建立完善大学英语口语教学文件（如教学大纲、教学目标、课程描述、教学安排、教学进度、考核方式等），进一步确立 以 现代化英语教学理论为指导、以教学实验与研究为核心、对英语口语和教学作全方位探索的精品课建设原则和模式。我们尚须 进一步以口语为突破口，逐步改变大学英语忽视语言交际能力培养的历史状态，切实将学生的学习兴趣引至语言应用和交际能力的方向，借助口语精品课程建设内容，加强对大学生的人文教育，提高大学生的文化素质，并增强他们对祖国文化 ﹑ 历史和优秀民族传统的理解和继承能力。我们尚须 进一步紧随高教部的指示精神，以第二语言习得理论为指导，在加强学生自主学习和使用现代教育手段方面作更深入细致的研究，将精品口语课程中关键元素作分析，细化，提炼，强化和推广，更有效地渗透并融合进阅读，写作，听力，翻译等其他方面的教学 , 加强与国内外高校和学术机构的联系 , 以论文和专著形式 总结以大学英语口语精品课程建设为龙头 <strong>促进大学英语教学模式深化改革 </strong>的成果， <strong>立志成为 国家级精品课程 </strong>。 </p>
                <p align="left">总<span class="MsoNormal style4">之，口语精品课程建设既任重道远，又需以只争朝夕的精神来对待。我们课程组全体教师誓将 <strong>齐心协力，精诚合作，不辞辛苦，不断创新 </strong>，力求以口语精品课程建设为基点，为大学公共英语课程的教学改革作更大的贡献，实现教育部高教司关于“大学英语课程教学要求（试行）的通知”提出的目标。　 </span> </p></td>
